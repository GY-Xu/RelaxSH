def normalize_language(language: str | None) -> str:
    """Return a supported language code."""

    if language in SUPPORTED_LANGUAGES:
        return language
    return "zh"


def tr(language: str | None, key: str, **kwargs: object) -> str:
    """Translate a UI key into the requested language."""

    normalized = normalize_language(language)
    template = TRANSLATIONS[normalized].get(key) or TRANSLATIONS["zh"].get(key) or key
    return template.format(**kwargs)
